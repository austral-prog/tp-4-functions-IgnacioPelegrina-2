def number_to_month(month):
    if 1 <= month <= 12:
        if month == 1:
            print("enero")
        if month == 2:
            print("febrero")
        if month == 3:
            print("marzo")
        if month == 4:
            print("abril")
        if month == 5:
            print("mayo")
        if month == 6:
            print("junio")
        if month == 7:
            print("julio")
        if month == 8:
            print("agosto")
        if month == 9:
            print("septembre")
        if month == 10:
            print("octubre")
        if month == 11:
            print("noviembre")
        if month == 12:
            print("diciembre")
    else:
        print("error")